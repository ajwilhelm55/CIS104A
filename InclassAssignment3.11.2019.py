def maximumnumbers(integerA, integerB, integerC):
    return max(integerA, integerB, integerC)

print("This program finds the max of the numbers inputted:")

integerA = int(input("Type the first number:"))
integerB = int(input("Type the second number:"))
integerC = int(input("Type the third number:"))


print(maximumnumbers(integerA, integerB, integerC))
